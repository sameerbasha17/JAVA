// TASK : Write a program in which the program gives output numbers 1 to 10 along with their square roots.

public class NumberSqrt 
{
    public static void main(String [] args)
    {
        System.out.println("Number\tSqrt");
        System.out.println("------\t----");
        System.out.println(System.lineSeparator() + "1\t" + Math.sqrt(1));
        System.out.println( "2\t" + Math.pow(2,0.5));
        System.out.println( "3\t" + Math.sqrt(3));
        System.out.println( "4\t" + Math.sqrt(4));
        System.out.println( "5\t" + Math.sqrt(5));
        System.out.println( "6\t" + Math.sqrt(6));
        System.out.println( "7\t" + Math.sqrt(7));
        System.out.println( "8\t" + Math.sqrt(8));
        System.out.println( "9\t" + Math.sqrt(9));
        System.out.println( "10\t" + Math.sqrt(10));
       
    }
}
